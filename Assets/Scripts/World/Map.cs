        public BoardTile[,] BoardTiles;

        [SerializeField] private List<GameObject> _mapPrefabs;

        private float _worldPosOffset = 5f;

        private Biomes _biomeDatabase;

        private void Awake()
        {
            _biomeDatabase = GetComponent<Biomes>();
            GameObject mapPrefabClone;

            BoardTiles = new BoardTile[WorldMap.GetLongLength(0), WorldMap.GetLongLength(1)];

            BoardTile boardTile;

            for (int y = 0; y < WorldMap.GetLongLength(0); y++)
            {
                for (int x = 0; x < WorldMap.GetLongLength(1); x++)
                {
                    if(WorldMap[y, x] != 0)
                    {
                        mapPrefabClone = Instantiate(_mapPrefabs[WorldMap[y, x]]);
                        mapPrefabClone.transform.position = BoardToWorldPos(new int2(x, y));
                        mapPrefabClone.transform.SetParent(transform);
                        
                        if(WorldMap[y, x] > 2)
                        {
                            BoardTiles[y, x] = mapPrefabClone.GetComponent<BoardTile>();
                            boardTile = mapPrefabClone.GetComponent<BoardTile>();
                            boardTile.BoardPosition = new int2(x, y);
                            boardTile.BattleBiome = _biomeDatabase.GetBiomeByIndex(_biomeDatabase.BiomeMap[y, x]);
                        }   
                    }
                }
            }
        }

        public Vector2 BoardToWorldPos(int2 boardPos)
        {
            Vector2 worldPos = new Vector2((transform.position.x + (boardPos.x * _worldPosOffset))/*/2*/, (transform.position.y - (boardPos.y * _worldPosOffset))/*/2*/);
            return worldPos;
        }
